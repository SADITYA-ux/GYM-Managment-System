// Helper function to get unique categories
export const getCategories = () => {
  const categories = [...new Set(foodData.map(food => food.category))];
  return categories.map(cat => cat.charAt(0).toUpperCase() + cat.slice(1));
};

// Helper function to filter by category
export const getFoodsByCategory = (category) => {
  if (category.toLowerCase() === 'all') return foodData;
  return foodData.filter(food => food.category === category.toLowerCase());
};

// Helper function to search foods
export const searchFoods = (query) => {
  if (!query) return foodData;
  return foodData.filter(food => 
    food.name.toLowerCase().includes(query.toLowerCase())
  );
};